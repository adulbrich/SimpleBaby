async function imageLoadingState(
    uploadResult: any,
    saveLogResult: any,
) {
    const manualPromise: () => {
        promise: Promise<unknown>, resolve: (value: any) => void
    } = () => {
        let resolvePromise: (value: any) => void = () => undefined;
        const promise = new Promise((resolve) => {
            resolvePromise = (value: any) => resolve(value);
        });
        return { promise, resolve: resolvePromise };
    };

    const { promise: waitForUpload, resolve: resolveUpload } = manualPromise();
    const { promise: waitForUploadCalled, resolve: resolveUploadCalled } = manualPromise();

    // supabase.storage.from().upload() should be mocked to return:
    // {error: /* truthy string */}
    // This should cause error handling in library/log-functions -> createRemoteLog()
    (supabase.storage.from("").upload as jest.Mock).mockImplementationOnce(() => {
        resolveUploadCalled(undefined);
        return waitForUpload;
    });

    const setPhotoLoadingState = jest.fn();

    const result = saveLog({
        tableName: "feeding_logs",
        fields: [{
            dbFieldName: "",
            value: "",
            type: "photo",
        }],
    }, false, "", setPhotoLoadingState);

    expect(result instanceof Promise).toBe(true);

    await waitForUploadCalled;  // wait for supabase.storage.from("").upload() to be called
    expect(setPhotoLoadingState).toHaveBeenLastCalledWith(true);
    expect(setPhotoLoadingState).toHaveBeenLastCalledWith(true);  // photo has begun loading

    let uploadComplete = false;
    setTimeout(() => {
        expect(setPhotoLoadingState).toHaveBeenLastCalledWith(true);  // photo is still loading
        resolveUpload(uploadResult);  // unblock supabase.storage.from().upload()
        uploadComplete = true;
    }, 500);

    const resolvedResult = await result;
    expect(uploadComplete).toBe(true);  // timeout has completed
    expect(resolvedResult).toEqual(saveLogResult);
    expect(setPhotoLoadingState).toHaveBeenLastCalledWith(false);  // photo is no longer loading
}

