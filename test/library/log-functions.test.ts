async function savesTableName(isGuest: boolean) {
    const tableNames = [
        "feeding_logs",
        "nursing_logs",
        "milestone_logs",
        "diaper_logs",
        "health_logs",
        "sleep_logs",
    ];

    for (const tableName of tableNames) {
        const result = await saveLog({
            tableName: tableName as TableName,
            fields: [],
        }, isGuest, "");

        expect(result.success).toBe(true);  // ensure log was saved successfully

        if (isGuest) {
            // Ensure library/local-store.ts -> was called with the correct table
            expect((insertRow as jest.Mock).mock.calls[0][0]).toBe(tableName);
            (insertRow as jest.Mock).mockClear();  // reset .mock.calls array for the next iteration
        } else {
            // Ensure supabase.from().insert() was called with the correct values; they should now be encrypted
            expect(supabase.from as jest.Mock).toHaveBeenLastCalledWith(tableName);
        }
    }
}


